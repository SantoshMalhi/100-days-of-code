# Stack example
stack = []

# Adding elements (push operation)
stack.append(1)
stack.append(2)
stack.append(3)
print(stack)  # Output: [1, 2, 3]

# Removing elements (pop operation)
print(stack.pop())  # Output: 3
print(stack)  # Output: [1, 2]
print(stack.pop())  # Output: 2
print(stack)  # Output: [1]
